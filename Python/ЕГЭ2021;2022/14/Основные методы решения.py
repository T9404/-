    num_cc = ''

    while num:
        num_cc += str(num % cc)
        num //= cc

    # —Å–ø—Ä–æ—Å–∏–ª–∏ –ø—Ä–æ –ø–µ—Ä–≤—É—é —Ü–∏—Ñ—Ä—É, –∞ –≤—ã –Ω–µ –ø–æ–≤–µ—Ä–Ω—É–ª–∏ —á–∏—Å–ª–æ? –º–∏–Ω—É—Å –±–∞–ª–ª
    num_cc = num_cc[::-1]  
    return num_cc


